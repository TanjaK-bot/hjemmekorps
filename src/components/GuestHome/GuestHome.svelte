
<p>
	Hjemmekorps skal inspirere korpsmusikanter til hjemmeøving ved å gjøre leksene til en del av felles prosjekter. Dirigenten legger inn noter, musikantene spiller inn stemmene hjemme og får høre det kombinerte resultatet - enten rett etter innspilling eller på neste øvelse.
</p>

<p>...det kan også brukes under pandemier til å gi en liten fornemmelse av samspill..</p>

<p>
