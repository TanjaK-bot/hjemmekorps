<h1>Om Hjemmekorps.no</h1>

<p>
  Hjemmekorps.no er et eksperiment, inspirert av både korona og en generell
  frustrasjon over at så mange musikanter faller fra underveis. Sannsynligvis
  har en del av det å gjøre med at de øver for lite og ikke opplever nok
  mestring.
</p>

<p>
  Hjemmekorps.no skal inspirere til hjemmeøving ved å gjøre leksene til en del
  av felles prosjekter. Det er ment som et verktøy for dirigenter og
  instruktører. Målet er selvfølgelig større mestringsfølelse og mindre frafall
  blandt de yngste - men det kan også brukes mot pandemi og sosial isolasjon for
  både små og store.
</p>
<p>
  Øve-opptak hjemmefra settes automatisk sammen til små låter som kan spilles av
  enten rett etter opptaket eller på neste samspill-øvelse. Det eneste
  dirigenten trenger å gjøre, er å laste opp noter i MusicXML-format og sende ut
  lenker til musikantenes opptaks-sider.
</p>
<p>For å få bruke Hjemmekorps.no til ditt korps eller orkester, ta kontakt.</p>

