Hjemmekorps skal inspirere til hjemmeøving ved å gjøre leksene til en del av felles prosjekter. Øve-opptak hjemmefra settes automatisk sammen til små låter som kan spilles av på neste samspill-øvelse. Det eneste dirigenten trenger å gjøre, er å laste opp noter i MusicXML-format.
