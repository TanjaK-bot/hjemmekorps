<h1>Bruk av personlige opplysninger</h1>
<p>
Hjemmekorps samler bare inn et minimum av personopplysninger, som er nødvendige for å drive tjenesten. Vi lagrer bare data i din nettleser for å huske at du er logget inn. Vi bruker ingen tredjeparts-tjenester for analyse eller sporing.
</p>
<p>
Data om prosjekter og brukere lagres i Sanity.io, som er vår eneste leverandør.
</p>
<p>
Etter personopplysningslovens § 18, 1. ledd, har du på forespørsel rett til innsyn i egne opplysninger. Du har også rett til å be om at informasjon slettes eller rettes.
</p>

<p>
Dersom du har ytterligere spørsmål om Hjemmekorps og personvern, eller ønsker å bruke systemet for ditt korps eller orkester, <a href="/om/kontakt">ta kontakt</a>.
</p>